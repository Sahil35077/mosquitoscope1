_DISEASE_PATTERNS = [
    ("dengue", "Dengue"), ("zika", "Zika"), ("chikungunya", "Chikungunya"),
    ("yellow fever", "Yellow fever"), ("malaria", "Malaria"), ("west nile", "West Nile"),
    ("la crosse", "La Crosse encephalitis"), ("eastern equine", "EEE"),
    ("encephalitis", "Encephalitis"), ("heartworm", "Heartworm"), ("filariasis", "Filariasis"),
]


def _extract_genus(class_name: str) -> str:
    if class_name.startswith("Ae_"):
        return "Aedes"
    return class_name.split("_")[0]


def _infer_risk(vector_status: str) -> tuple[str, str]:
    text = vector_status.lower()
    if any(k in text for k in ("major vector", "primary vector", "malaria")):
        return "High", "risk-high"
    if "vector of" in text or "can carry" in text or "can transmit" in text:
        return "Moderate", "risk-moderate"
    if "nuisance" in text or "non-biting" in text or "limited" in text:
        return "Low", "risk-low"
    return "Moderate", "risk-moderate"


def _infer_diseases(vector_status: str) -> list[str]:
    text = vector_status.lower()
    found = [label for key, label in _DISEASE_PATTERNS if key in text]
    if found:
        return list(dict.fromkeys(found))
    if "nuisance" in text:
        return ["Primarily nuisance biting"]
    if "non-biting" in text:
        return ["None — adults do not blood-feed"]
    return ["Consult local health department data"]


def get_fact(class_name: str) -> dict:
    base = FACTS.get(class_name, {
        "common_name": class_name.replace("_", " "),
        "region": "See entomological references for distribution.",
        "vector_status": "Consult local surveillance data for medical importance.",
        "habitat": "Freshwater-associated mosquito habitat.",
        "fact": "Classification based on ConvNeXt V2 model trained on mosquito image dataset.",
    })
    genus = _extract_genus(class_name)
    meta = GENUS_META.get(genus, {
        "biting_time": "Varies by species and season",
        "size": "Typically 4–8 mm body length",
        "resting_posture": "Species-specific resting angle",
    })
    risk_label, risk_class = _infer_risk(base["vector_status"])
    return {
        **base,
        "genus": genus,
        "family": "Culicidae",
        "scientific_name": class_name.replace("_", " "),
        "biting_time": base.get("biting_time", meta["biting_time"]),
        "size": base.get("size", meta["size"]),
        "resting_posture": base.get("resting_posture", meta["resting_posture"]),
        "risk_level": base.get("risk_level", risk_label),
        "risk_class": risk_class,
        "diseases": base.get("diseases", _infer_diseases(base["vector_status"])),
        "identification": base.get("identification", base["fact"]),
    }
